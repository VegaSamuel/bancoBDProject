    private void cargarTablaRetiros() {
        try {
            List<Retiro> listaRetiros = this.retiroDAO.consultar(configPaginado);
            DefaultTableModel modeloTabla = (DefaultTableModel) tblRetiros.getModel();
            listaRetiros.forEach(retiro -> {
                Object[] fila = {
                    retiro.getId_retiro(),
                    retiro.getMonto_retirado(),
                    retiro.getFolio(),
                    retiro.getCuentaRetiro()};
                modeloTabla.addRow(fila);
            });
        }catch(DAOException e) {
            LOG.log(Level.SEVERE, "No se pudo crear la lista ",e.getMessage());
        }
    }
    
    private void siguientePagina() {
        configPaginado.avanzarPagina();
        cargarTablaRetiros();
    }
    
    private void paginaAnterior() {
        configPaginado.retrocederPagina();
        cargarTablaRetiros();
    }

